/*
Arrays (Vetores)
  - É uma estrutura no js para trabalhar com todo tipo de lista;
  - Seus elementos são indicados por um indice inteiro e começa no 0;
  - Pode ser criado através de colchetes [];
  - Pode ser de qualquer tipo e tamanho 
*/

const listaDeCompras = [];
console.log(listaDeCompras);

listaDeCompras[0] = "Arroz";

console.log(listaDeCompras);

listaDeCompras[1] = "Feijão";

console.log(listaDeCompras);

listaDeCompras.push = "Batata";

console.log(listaDeCompras);
